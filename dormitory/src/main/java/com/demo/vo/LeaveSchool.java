package com.demo.vo;

public class LeaveSchool {

    private Integer id;

    private String number;

    private String name;

    private String dormitory;

    private String leaveSchoolTime;

    private String reason;

    private String valuables;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDormitory() {
        return dormitory;
    }

    public void setDormitory(String dormitory) {
        this.dormitory = dormitory;
    }

    public String getLeaveSchoolTime() {
        return leaveSchoolTime;
    }

    public void setLeaveSchoolTime(String leaveSchoolTime) {
        this.leaveSchoolTime = leaveSchoolTime;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getValuables() {
        return valuables;
    }

    public void setValuables(String valuables) {
        this.valuables = valuables;
    }
}
